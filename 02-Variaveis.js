// Tipos de variáveis
// 'let' ou 'var', podendo declarar dessas duas formas, (Não utilize o var)
// 'const' uma variável também, mas não pode alterar diferente do 'let'

idade = 30                // Number
let numero = 19          // Number
let altura = 1.82       // Number
let nome = 'João'      // String(nome)
let estudando = true  // Booleano (true ou false)


console.log(idade, typeof idade)
console.log(numero, typeof numero)
console.log(altura, typeof altura)
console.log(nome, typeof nome)
console.log(estudando, typeof estudando)